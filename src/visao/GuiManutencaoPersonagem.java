    private void preencherTabela() {
        try {
            ArrayList<PersonagemVO> personagens = new ArrayList<>();
            PersonagemServicos ps = ServicosFactory.getPersonagemServicos();
            personagens = ps.buscarPersonagens();

            limparTabela();
            
            for (int i = 0; i < personagens.size(); i++) {
                dtm.addRow(new String[]{
                    String.valueOf(personagens.get(i).getIdPersonagem()),
                    personagens.get(i).getNome(),
                    personagens.get(i).getClasse()
                });
            }
            jtablePersonagem.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro! " + e.getMessage());
        }
    }

    private void limparTabela() {
        dtm.setNumRows(0);
    }
    
    private void filtrar() {
        try {
            if (jtPesquisa.getText().isEmpty()) {
                limparTabela();
                preencherTabela();
                return;
            }
            
            String pesquisa = jtPesquisa.getText();
            String filtro = jcbFiltro.getSelectedItem().toString();
            PersonagemServicos ps = ServicosFactory.getPersonagemServicos();
            ArrayList<PersonagemVO> prods = ps.filtrar(pesquisa, filtro);
            
            for(int i=0 ; i<prods.size() ; i++) {
                dtm.addRow(new String[]{
                    String.valueOf(prods.get(i).getIdPersonagem()),
                    prods.get(i).getNome(),
                    prods.get(i).getClasse(),
                });
            }
            
            /* Adicionando o modelo de tabela 
             com os dados na tabela produto */
            jtablePersonagem.setModel(dtm);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro!" +e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }//fecha catch
    }//fecha filtrar
    
    private void deletar() {
        try {
            int linha = jtablePersonagem.getSelectedRow();
            if (linha == -1) {
                JOptionPane.showMessageDialog(this, "selecione uma linha para excluir!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }//fecha if
            
            String id = jtablePersonagem.getValueAt(linha, 0).toString();
            
            PersonagemServicos ps = ServicosFactory.getPersonagemServicos();
            ps.deletarPersonagem(Long.parseLong(id));
            
            JOptionPane.showMessageDialog(this, "Personagem excluído com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro!" +e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//fecha deletar

